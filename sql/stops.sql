CREATE TABLE stops(
    id INTEGER PRIMARY KEY,
    station_id INTEGER,
    city_code CHAR(10),
    city_name CHAR(50),
    metropolin_code CHAR(10),
    metropolin_name CHAR(50),
    station_type_code CHAR(10),
    station_type_name CHAR(50),
    latitude CHAR(10),
    longitude CHAR(10),
    neighborhood CHAR(50)
)
 